// Eligibility verdict against a context profile. Returns 'green' | 'orange' | 'red'.
export function schemeEligibility(scheme, ctx = {}) {
  const { state = 'all', category, appliesTo } = ctx;
  const stateOk = scheme.states.includes('all') || scheme.states.includes(state);
  const catOk = !category || scheme.categories.includes(category);
  const useOk = !appliesTo || scheme.appliesTo.includes(appliesTo);

  if (stateOk && catOk && useOk) return 'green';
  if (stateOk && (catOk || useOk)) return 'orange';
  return 'red';
}

